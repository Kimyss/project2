//주석 단축키 - 블럭선택 > 컨트롤 + /
//주석 단축키 - 블럭선택 > 컨트롤 + 쉬프트 + /
public class Ex1 {

	
	  public static void main(String[] args) {
		  
		  int x;	//정수형 변수 선언
		  
		  x = 10;	//x에 10을 대입
		  
		  System.out.println(x); //x를 출력하면 변수에 담긴 값이 출력됨
		  
		  //sysout 작성 + 컨트롤키 + 스페이스키
		  System.out.println();
		  
		  System.out.println("안녕하세요");		//println 출력 후 줄바꿈 실행
		  
		  System.out.print("반갑습니다");		//print 줄바꿈 실행안함
		  
		  System.out.print("반갑습니다");
	 }
	 
}